def create_beautiful_badge_poster(badge: dict) -> str:
    """
    Create beautiful badge showcase poster
    
    Args:
        badge: Dictionary with badge info (name, rarity, icon, earned_at)
        
    Returns:
        Base64 encoded PNG image string
    """
    logger.info("Creating beautiful badge poster...")
    
    width, height = 600, 700
    img = _create_gradient_background(width, height, (255, 215, 0), (255, 165, 0))
    draw = ImageDraw.Draw(img)
    
    # Header
    header_y = 40
    _draw_rounded_rect(draw, (30, header_y, 570, header_y + 100), 20, fill="white")
    
    title_font = _get_font(36, bold=True)
    draw.text((50, header_y + 30), "Badge Showcase", fill=(255, 165, 0), font=title_font)
    
    # Badge display area
    badge_y = 170
    _draw_rounded_rect(draw, (50, badge_y, 550, badge_y + 350), 25, fill="white")
    
    if badge:
        icon = badge.get("icon", "\U0001f3c6") or "\U0001f3c6"
        name = badge.get("name", "Unknown Badge")
        rarity = badge.get("rarity", "Common")
        earned_at = badge.get("earned_at", "")
        
        # Large icon display
        icon_font = _get_font(120)
        icon_center_x = 300
        icon_center_y = badge_y + 120
        draw.text((icon_center_x - 40, icon_center_y - 50), icon, fill=(255, 215, 0), font=icon_font)
        
        # Badge name
        name_font = _get_font(32, bold=True)
        name_bbox = draw.textbbox((0, 0), name, font=name_font)
        name_width = name_bbox[2] - name_bbox[0]
        draw.text((icon_center_x - name_width//2, badge_y + 240), name, fill=(50, 50, 50), font=name_font)
        
        # Rarity badge
        rarity_colors = {
            "Common": (169, 169, 169),
            "Rare": (0, 191, 255),
            "Epic": (148, 0, 211),
            "Legendary": (255, 69, 0),
        }
        rarity_color = rarity_colors.get(rarity, (169, 169, 169))
        
        rarity_font = _get_font(24, bold=True)
        rarity_text = f"[{rarity}]"
        rarity_bbox = draw.textbbox((0, 0), rarity_text, font=rarity_font)
        rarity_width = rarity_bbox[2] - rarity_bbox[0]
        draw.text((icon_center_x - rarity_width//2, badge_y + 290), rarity_text, fill=rarity_color, font=rarity_font)
        
        # Earned date
        if earned_at:
            date_font = _get_font(16)
            draw.text((icon_center_x - 60, badge_y + 330), f"Earned: {earned_at}", fill=(120, 120, 120), font=date_font)
    
    # Save and encode
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    timestamp = date.today().strftime("%Y%m%d_%H%M%S")
    filename = f"badge_{timestamp}.png"
    filepath = OUTPUT_DIR / filename
    
    img.save(filepath, "PNG")
    logger.info(f"Badge poster saved to: {filepath}")
    
    buffer = io.BytesIO()
    img.save(buffer, format="PNG")
    base64_str = __import__("base64").b64encode(buffer.getvalue()).decode("utf-8")
    
    logger.info("Beautiful badge poster generated successfully")
    return base64_str
